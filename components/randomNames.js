

function randomNames() {
    const names = [
        "Plipp",
        "Plupp",
        "Plopp",
        "Plepp",
        "Plapp",
        "Pomp",
        "Panu",
        "Pombo",
        "Pumpe",
        "Peh",
        "Phong",
        "Puq"
    ];
    return names[Math.floor(Math.random()*names.length)];
}

export default randomNames;

